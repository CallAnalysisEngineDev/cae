package org.cae.entity;

public class CallRecord {

	private String callId;
	private String callName;
	private String callContent;
	private String callSellTime;
	private Short callVersion;
	private String callCover;
	private String callLastModify;
	public String getCallId() {
		return callId;
	}
	public void setCallId(String callId) {
		this.callId = callId;
	}
	public String getCallName() {
		return callName;
	}
	public void setCallName(String callName) {
		this.callName = callName;
	}
	public String getCallContent() {
		return callContent;
	}
	public void setCallContent(String callContent) {
		this.callContent = callContent;
	}
	public String getCallSellTime() {
		return callSellTime;
	}
	public void setCallSellTime(String callSellTime) {
		this.callSellTime = callSellTime;
	}
	public Short getCallVersion() {
		return callVersion;
	}
	public void setCallVersion(Short callVersion) {
		this.callVersion = callVersion;
	}
	public String getCallCover() {
		return callCover;
	}
	public void setCallCover(String callCover) {
		this.callCover = callCover;
	}
	public String getCallLastModify() {
		return callLastModify;
	}
	public void setCallLastModify(String callLastModify) {
		this.callLastModify = callLastModify;
	}
	
}
